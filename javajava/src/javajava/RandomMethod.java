package javajava;

public class RandomMethod {
  
	public static void main(String[] args) {
		String postion = "과장";
		
		switch(postion) {
		case "과장" :
			System.out.println("과장 입니다.");
			break;
		case "소장" : 
			System.out.println("소장 입니다.");
			break;
		case "신입" :
			System.out.println("신입 입니다.");
			break;
		case "병사" :
			System.out.println("병사");
			break;
		}
		
	}
}
